function AddTask() {
    var black = ("rgb(" + 0 + "," + 0 + "," + 0 + ")");
    var UserInput = prompt("Enter the Task")

    var task = document.createElement('p')
    var task1 = document.getElementById("Task1")    
    task.innerText = UserInput
    task1.appendChild(task).style.color=black    
}

function Done() {
    var checkbox = document.getElementById("checkbox")
    if (checkbox.checked) {
        alert("Congratulations! You have done the Task.")
    }
}

function Remove() {
    document.getElementById("Task1").innerText = " "
}



