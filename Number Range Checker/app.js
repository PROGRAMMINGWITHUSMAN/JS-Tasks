var NumberInput = prompt("Type number")

if (NumberInput >= 1 && NumberInput <= 10) {
    document.write("The number is between 1 and 10.")
}
else if (NumberInput >= 11 && NumberInput <= 20) {
    document.write("The number is between 11 and 20.")
}
else if (NumberInput >= 21 && NumberInput <= 30) {
    document.write("The number is between 21 and 30.")   
}
else if (NumberInput >= 31 && NumberInput <= 40) {
    document.write("The number is between 31 and 40.")
}
else if (NumberInput >= 41 && NumberInput <= 50) {
    document.write("The number is between 41 and 50.")
}
else if (NumberInput >= 51 && NumberInput <= 60) {
    document.write("The number is between 51 and 60.")
}
else if (NumberInput >= 61 && NumberInput <= 70) {
    document.write("The number is between 61 and 70.")
}
else if (NumberInput >= 71 && NumberInput <= 80) {
    document.write("The number is between 71 and 80.")
}
else if (NumberInput >= 81 && NumberInput <= 90) {
    document.write("The number is between 81 and 90.")
}
else if (NumberInput >= 91 && NumberInput <= 100) {
    document.write("The number is between 91 and 100.")
}
else{
    document.write("The number is out of range.")
}